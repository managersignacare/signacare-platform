// apps/api/src/features/llm/letterRoutes.ts
//
// Tier 15 — letter authoring routes.
//
// State machine:
//   draft → in_review → approved → sent
//          ↳ revised (rejected) → in_review → ...
//          ↳ withdrawn (terminal)
//
// Every state-changing endpoint delegates to letterService so the
// letters row + letter_sections rows + letter_audit_log row are
// written inside the same transaction.

import { Router, type Request, type Response, type NextFunction } from 'express';
import { z } from 'zod';
import { authMiddleware } from '../../middleware/authMiddleware';
import { requireRoles } from '../../middleware/rbacMiddleware';
import { requireFeatureEnabled } from '../../middleware/featureFlagMiddleware';
import { db } from '../../db/db';
import { buildAuthContext } from '../../shared/buildAuthContext';
import { requirePatientRelationship } from '../../shared/authGuards';
import {
  createDraftLetter, regenerateSection, editSection,
  submitForReview, approveLetter, rejectLetter,
} from './letterService';
import {
  deliverLetter, exportLetter, openRevision,
} from './letterDeliveryService';

const router = Router();
router.use(authMiddleware);
// Letter authoring is gated by the same scribe kill-switch because
// the sections are generated by the same model surface. If AI is
// paused, manual letter authoring still works (via editSection) but
// regeneration is blocked.
router.use(requireFeatureEnabled('ai-scribe'));

// ── Templates ──────────────────────────────────────────────────────────────

router.get('/templates',
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const category = typeof req.query.category === 'string' ? req.query.category : undefined;
      let q = db('letter_templates')
        .where(function () {
          this.whereNull('clinic_id').orWhere({ clinic_id: req.clinicId });
        })
        .andWhere({ is_active: true })
        .select(
          'id', 'clinic_id as clinicId', 'code', 'name', 'category',
          'description', 'sections', 'is_active as isActive',
          'requires_second_review as requiresSecondReview',
        )
        .orderBy([{ column: 'category', order: 'asc' }, { column: 'name', order: 'asc' }]);
      if (category) q = q.andWhere({ category });
      const rows = await q;
      res.json({ templates: rows });
    } catch (err) { next(err); }
  },
);

// ── Letters ────────────────────────────────────────────────────────────────

const CreateLetterSchema = z.object({
  templateId: z.string().uuid(),
  patientId: z.string().uuid(),
  episodeId: z.string().uuid().optional(),
  sessionId: z.string().uuid().optional(),
  subject: z.string().min(1).max(300),
  recipients: z.array(z.object({
    name: z.string().min(1).max(200),
    address: z.string().max(500).optional(),
    email: z.string().email().max(200).optional(),
    role: z.string().max(60).optional(),
  })).optional(),
});

router.post('/',
  requireRoles(['clinician', 'admin', 'superadmin']),
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const dto = CreateLetterSchema.parse(req.body);
      const auth = buildAuthContext(req, dto.patientId);
      await requirePatientRelationship(auth, dto.patientId);
      const result = await createDraftLetter(auth, dto);
      res.status(201).json(result);
    } catch (err) { next(err); }
  },
);

router.get('/',
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const status = typeof req.query.status === 'string' ? req.query.status : undefined;
      const patientId = typeof req.query.patientId === 'string' ? req.query.patientId : undefined;
      let q = db('letters')
        .where({ clinic_id: req.clinicId })
        .select(
          'id', 'template_id as templateId', 'patient_id as patientId',
          'episode_id as episodeId', 'author_id as authorId',
          'status', 'subject', 'revision',
          'approved_by as approvedBy', 'approved_at as approvedAt',
          'sent_by as sentBy', 'sent_at as sentAt',
          'created_at as createdAt', 'updated_at as updatedAt',
        )
        .orderBy('updated_at', 'desc')
        .limit(200);
      if (status) q = q.andWhere({ status });
      if (patientId) q = q.andWhere({ patient_id: patientId });
      const rows = await q;
      res.json({ letters: rows });
    } catch (err) { next(err); }
  },
);

router.get('/:id',
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const letter = await db('letters')
        .where({ id: req.params.id, clinic_id: req.clinicId })
        .first();
      if (!letter) { res.status(404).json({ error: 'Not found' }); return; }
      const sections = await db('letter_sections')
        .where({ letter_id: letter.id, clinic_id: req.clinicId })
        .select(
          'id', 'section_key as sectionKey', 'label', 'content',
          'section_order as sectionOrder', 'regen_count as regenCount',
          'last_regen_at as lastRegenAt', 'last_regen_by as lastRegenBy',
        )
        .orderBy('section_order', 'asc');
      res.json({
        letter: {
          id: letter.id,
          clinicId: letter.clinic_id,
          templateId: letter.template_id,
          patientId: letter.patient_id,
          episodeId: letter.episode_id,
          authorId: letter.author_id,
          sessionId: letter.session_id,
          status: letter.status,
          subject: letter.subject,
          recipients: letter.recipients,
          renderedText: letter.rendered_text,
          approvedBy: letter.approved_by,
          approvedAt: letter.approved_at,
          sentBy: letter.sent_by,
          sentAt: letter.sent_at,
          revision: letter.revision,
          createdAt: letter.created_at,
          updatedAt: letter.updated_at,
        },
        sections,
      });
    } catch (err) { next(err); }
  },
);

// ── Section regeneration / edit ────────────────────────────────────────────

const SectionPatchSchema = z.object({
  content: z.string().max(20_000),
  source: z.enum(['regenerated', 'edited']).default('edited'),
});

router.patch('/:id/sections/:sectionKey',
  requireRoles(['clinician', 'admin', 'superadmin']),
  async (req: Request, res: Response, next: NextFunction) => {
    try {
      const { content, source } = SectionPatchSchema.parse(req.body);
      const auth = buildAuthContext(req);
      const fn = source === 'regenerated' ? regenerateSection : editSection;
      const result = await fn(auth, req.params.id, req.params.sectionKey, content);
      res.json(result);
    } catch (err) { next(err); }
  },
);

