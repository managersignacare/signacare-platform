          'recipient_address as recipientAddress',
          'recipient_email as recipientEmail',
          'recipient_fax as recipientFax',
          'recipient_mhr_ihi as recipientMhrIhi',
          'status', 'receipt_id as receiptId',
          'sent_by as sentBy', 'attempted_at as attemptedAt',
