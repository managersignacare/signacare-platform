const GST_RATE = 0.1;

function notFound(msg: string, code: string): Error & { status: number; code: string } {
  const err = new Error(msg) as Error & { status: number; code: string };
  err.status = 404;
  err.code = code;
  return err;
}

function badRequest(msg: string, code: string): Error & { status: number; code: string } {
  const err = new Error(msg) as Error & { status: number; code: string };
  err.status = 400;
  err.code = code;
  return err;
}

function mapAccount(row: Record<string, unknown>): BillingAccountResponse {
  return {
    id: row['id'] as string,
    clinicId: row['clinic_id'] as string,
    patientId: row['patient_id'] as string,
    billingType: row['billing_type'] as BillingAccountResponse['billingType'],
    healthFundName: (row['health_fund_name'] as string | undefined) ?? undefined,
    healthFundMemberNumber:
      (row['health_fund_member_number'] as string | undefined) ?? undefined,
    ndisNumber: (row['ndis_number'] as string | undefined) ?? undefined,
    ndisPackageManager:
      (row['ndis_package_manager'] as string | undefined) ?? undefined,
    dvaNumber: (row['dva_number'] as string | undefined) ?? undefined,
    dvaCardType:
      (row['dva_card_type'] as BillingAccountResponse['dvaCardType']) ?? undefined,
    notes: (row['notes'] as string | undefined) ?? undefined,
    createdAt: row['created_at'] as string,
    updatedAt: row['updated_at'] as string,
  };
}

function mapInvoice(
  row: Record<string, unknown> & { lineItems: Record<string, unknown>[] },
): InvoiceResponse {
  return {
    id: row['id'] as string,
    clinicId: row['clinic_id'] as string,
    patientId: row['patient_id'] as string,
    clinicianId: (row['clinician_id'] as string | null) ?? null,
    appointmentId: (row['appointment_id'] as string | null) ?? null,
    invoiceNumber: row['invoice_number'] as string,
    billingType: (row['billing_type'] as string) ?? 'private',
    subtotalCents: Number(row['subtotal_cents'] ?? row['subtotal'] ?? 0),
    gstCents: Number(row['gst_cents'] ?? row['gst_amount'] ?? 0),
    totalCents: Number(row['total_cents'] ?? row['total_amount'] ?? 0),
    paidCents: Number(row['paid_cents'] ?? row['paid_amount'] ?? 0),
    gapCents: Number(row['gap_cents'] ?? 0),
    scheduleFeeCents: Number(row['schedule_fee_cents'] ?? 0),
    rebateCents: Number(row['rebate_cents'] ?? 0),
    providerFeeCents: Number(row['provider_fee_cents'] ?? 0),
    status: (row['status'] as string) ?? 'draft',
    dueDate: (row['due_date'] as string | null) ?? null,
    notes: (row['notes'] as string | null) ?? null,
    autoGenerated: Boolean(row['auto_generated']),
    overrideNotes: (row['override_notes'] as string | null) ?? null,
    referralValid: row['referral_valid'] !== false,
    approvedAt: (row['approved_at'] as string | null) ?? null,
    approvedByStaffId: (row['approved_by_staff_id'] as string | null) ?? null,
    sentAt: (row['sent_at'] as string | null) ?? null,
    lineItems: (row.lineItems ?? []).map((li) => ({
      id: li['id'] as string,
      invoiceId: li['invoice_id'] as string,
      mbsItemNumber: (li['mbs_item_number'] as string | undefined) ?? (li['mbs_item_code'] as string | undefined),
      description: li['description'] as string,
      quantity: Number(li['quantity']),
      unitPriceCents: Number(li['unit_price_cents'] ?? li['unit_price'] ?? li['fee_cents'] ?? 0),
      discountCents: Number(li['discount_cents'] ?? li['discount_amount'] ?? 0),
      lineTotalCents: Number(li['line_total_cents'] ?? li['line_total'] ?? 0),
      scheduleFeeCents: Number(li['schedule_fee_cents'] ?? 0),
    })),
    createdAt: row['created_at'] as string,
    updatedAt: row['updated_at'] as string,
  };
}

function mapPayment(row: Record<string, unknown>): PaymentResponse {
  return {
    id: row['id'] as string,
    clinicId: row['clinic_id'] as string,
    invoiceId: row['invoice_id'] as string,
    receivedById: row['received_by_id'] as string,
    amount: Number(row['amount']),
    paymentMethod: row['payment_method'] as PaymentResponse['paymentMethod'],
    paymentDate: row['payment_date'] as string,
    referenceNumber: (row['reference_number'] as string | undefined) ?? undefined,
    claimStatus: (row['claim_status'] as PaymentResponse['claimStatus']) ?? null,
    claimReference: (row['claim_reference'] as string | null) ?? null,
    notes: (row['notes'] as string | undefined) ?? undefined,
    createdAt: row['created_at'] as string,
    updatedAt: row['updated_at'] as string,
  };
}

