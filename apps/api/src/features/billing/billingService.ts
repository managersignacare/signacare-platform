// ── Invoices ──────────────────────────────────────────────────────────────────

export async function createInvoice(
  clinicId: string,
  dto: InvoiceCreateDTO,
): Promise<InvoiceResponse> {
  const subtotal = dto.lineItems.reduce(
    (sum, li) => sum + li.unitPriceCents * li.quantity - (li.discountCents ?? 0),
    0,
  );
  // GST applies to private billing only; bulk-bill, DVA and NDIS are GST-free
  const gstAmount =
    dto.billingType === 'private'
      ? Math.round(subtotal * GST_RATE * 100) / 100
      : 0;
  const totalAmount = Math.round((subtotal + gstAmount) * 100) / 100;
  const invoiceNumber = await generateInvoiceNumber(clinicId);

  const row = await billingRepo.createInvoice(
    clinicId,
    dto,
    invoiceNumber,
    subtotal,
    gstAmount,
    totalAmount,
  );
  return mapInvoice(row);
}

export async function listInvoicesByPatient(
  clinicId: string,
  patientId: string,
): Promise<InvoiceResponse[]> {
  const rows = await billingRepo.findInvoicesByPatient(clinicId, patientId);
  return Promise.all(
    rows.map(async (r) => {
      const full = await billingRepo.getInvoiceWithItems(clinicId, r['id'] as string);
      return mapInvoice(full!);
    }),
  );
}

export async function getInvoice(
  clinicId: string,
  invoiceId: string,
): Promise<InvoiceResponse> {
  const row = await billingRepo.getInvoiceWithItems(clinicId, invoiceId);
  if (!row) throw notFound('Invoice not found', 'INVOICE_NOT_FOUND');
  return mapInvoice(row);
}

export async function voidInvoice(
  clinicId: string,
  invoiceId: string,
): Promise<void> {
  const row = await billingRepo.getInvoiceWithItems(clinicId, invoiceId);
  if (!row) throw notFound('Invoice not found', 'INVOICE_NOT_FOUND');
  if (row['paid_amount'] && Number(row['paid_amount']) > 0) {
    throw badRequest(
      'Cannot void an invoice with payments recorded',
      'INVOICE_HAS_PAYMENTS',
    );
  }
  await billingRepo.voidInvoice(clinicId, invoiceId);
}

// ── Payments ──────────────────────────────────────────────────────────────────

export async function recordPayment(
  clinicId: string,
  receivedById: string,
  dto: PaymentCreateDTO,
): Promise<PaymentResponse> {
  const invoice = await billingRepo.getInvoiceWithItems(clinicId, dto.invoiceId);
  if (!invoice) throw notFound('Invoice not found', 'INVOICE_NOT_FOUND');

  const status = invoice['status'] as string;
  if (status === 'void' || status === 'refunded') {
    throw badRequest(
      'Cannot record payment on a void or refunded invoice',
      'INVOICE_CLOSED',
    );
  }

  const remaining =
    Number(invoice['total_amount']) - Number(invoice['paid_amount']);
  if (dto.amount > remaining + 0.001) {
    throw badRequest(
      `Payment amount ${dto.amount} exceeds outstanding balance ${remaining.toFixed(2)}`,
      'PAYMENT_EXCEEDS_BALANCE',
    );
  }

  const row = await billingRepo.createPayment(clinicId, receivedById, dto);
  const payment = mapPayment(row);

  try {
    const patientId = (invoice['patient_id'] as string | null) ?? null;
    const invoiceNumber = (invoice['invoice_number'] as string | null) ?? null;
    if (patientId) {
      const amountCents = Math.max(0, Math.round(payment.amount));
      await jobBus.enqueue('email', {
        type: 'billing_notice',
        clinicId,
        patientId,
        invoiceId: dto.invoiceId,
        paymentId: payment.id,
        amountCents,
        currency: 'AUD',
        title: invoiceNumber ? `Payment receipt — ${invoiceNumber}` : 'Payment receipt',
        body: `We have received your payment of AUD ${(amountCents / 100).toFixed(2)}.`,
      });
    }
  } catch (err) {
    logger.warn({ err, clinicId, invoiceId: dto.invoiceId }, 'Failed to enqueue payment receipt email');
  }

  return payment;
}

