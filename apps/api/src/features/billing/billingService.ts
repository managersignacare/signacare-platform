// ── Auto-Invoice on Appointment Completion ────────────────────────────────────

/**
 * Auto-generate a draft invoice when an appointment is completed.
 * The clinician must review and approve before it's sent.
 */
export async function autoGenerateInvoice(
  clinicId: string,
  staffId: string,
  appointment: {
    id: string;
    patientId: string;
    clinicianId: string;
    type: string;
    startTime: string;
    endTime: string;
  },
): Promise<InvoiceResponse | null> {
  // 1. Suggest MBS item based on appointment
  const suggestion = await feeScheduleService.suggestMbsItem(
    clinicId, appointment.clinicianId, appointment,
  );
  if (!suggestion) {
    logger.info({ clinicId, appointmentId: appointment.id }, 'No MBS item suggestion — skipping auto-invoice');
    return null;
  }

  // 2. Check referral validity
  const { valid: referralValid } = await referralValidityService.checkValidity(
    clinicId, appointment.patientId,
  );

  // 3. Determine billing type from patient's billing account
  const account = await billingRepo.getBillingAccount(clinicId, appointment.patientId);
  const billingType = (account?.billing_type as string) ?? 'private';

  // 4. Calculate GST (private only)
  const gstCents = billingType === 'private'
    ? Math.round(suggestion.providerFeeCents * GST_RATE)
    : 0;
  const totalCents = suggestion.providerFeeCents + gstCents;

  // 5. Create draft invoice
  const invoiceNumber = await generateInvoiceNumber(clinicId);
  const today = todayLocal();
  const dueDate = new Date(today);
  dueDate.setDate(dueDate.getDate() + 30);
  const dueDateStr = dueDate.toISOString().split('T')[0];

  const row = await billingRepo.createAutoInvoice(clinicId, {
    patient_id: appointment.patientId,
    clinician_id: appointment.clinicianId,
    appointment_id: appointment.id,
    invoice_number: invoiceNumber,
    service_date: today,
    mbs_item_code: suggestion.itemNumber,
    mbs_item_description: suggestion.description,
    billing_type: billingType,
    subtotal_cents: suggestion.providerFeeCents,
    gst_cents: gstCents,
    total_cents: totalCents,
    paid_cents: 0,
    gap_cents: suggestion.gapCents,
    schedule_fee_cents: suggestion.scheduleFeeCents,
    rebate_cents: suggestion.rebateCents,
    provider_fee_cents: suggestion.providerFeeCents,
    status: 'draft',
    due_date: dueDateStr,
    auto_generated: true,
    referral_valid: referralValid,
    notes: suggestion.reason,
  }, [{
    mbs_item_code: suggestion.itemNumber,
    description: suggestion.description,
    quantity: 1,
    unit_price_cents: suggestion.providerFeeCents,
    discount_cents: 0,
    line_total_cents: suggestion.providerFeeCents,
    schedule_fee_cents: suggestion.scheduleFeeCents,
  }]);

  // 6. Create task for clinician to review
  try {
    await createTaskInternal(clinicId, staffId, {
      assignedToId: appointment.clinicianId,
      patientId: appointment.patientId,
      title: `Review draft invoice ${invoiceNumber}`,
      description: `Auto-generated invoice for ${suggestion.description}. Provider fee: $${(suggestion.providerFeeCents / 100).toFixed(2)}, Gap: $${(suggestion.gapCents / 100).toFixed(2)}${!referralValid ? ' — WARNING: No valid referral on file' : ''}`,
      priority: 'medium',
    });
  } catch (err) {
    logger.warn({ err, clinicId }, 'Failed to create invoice review task');
  }

  const full = await billingRepo.getInvoiceWithItems(clinicId, row.id as string);
  return full ? mapInvoice(full) : null;
}

// ── Invoice Approval ──────────────────────────────────────────────────────────

/**
 * Approve a draft invoice. Clinician can override MBS item and fee before approving.
 */
export async function approveInvoice(
  clinicId: string,
  staffId: string,
  invoiceId: string,
  dto: InvoiceApproveDTO,
): Promise<InvoiceResponse> {
  const invoice = await billingRepo.getInvoiceWithItems(clinicId, invoiceId);
  if (!invoice) throw notFound('Invoice not found', 'INVOICE_NOT_FOUND');

  const status = invoice['status'] as string;
  if (status !== 'draft' && status !== 'pending_approval') {
    throw badRequest('Only draft invoices can be approved', 'INVALID_STATUS');
  }

  // Apply overrides if provided
  const updates: Record<string, unknown> = {
    status: 'approved',
    approved_at: new Date(),
    approved_by_staff_id: staffId,
    updated_at: new Date(),
  };

  if (dto.overrideNotes) {
    updates.override_notes = dto.overrideNotes;
  }

  if (dto.overrideItemNumber || dto.overrideFeeCents !== undefined) {
    // Require override notes when changing the suggested item/fee
    if (!dto.overrideNotes) {
      throw badRequest('Override notes are required when changing the auto-suggested item or fee', 'OVERRIDE_NOTES_REQUIRED');
    }

    if (dto.overrideItemNumber) {
      const feeItem = await feeScheduleService.getByItemNumber(clinicId, dto.overrideItemNumber);
      if (feeItem) {
        updates.mbs_item_code = dto.overrideItemNumber;
        updates.mbs_item_description = feeItem.description;
        updates.schedule_fee_cents = feeItem.scheduleFeeCents;
        updates.rebate_cents = Math.round(feeItem.scheduleFeeCents * 0.85);
      }
    }

    if (dto.overrideFeeCents !== undefined) {
      const scheduleFee = (updates.schedule_fee_cents as number) ?? Number(invoice['schedule_fee_cents'] ?? 0);
      updates.provider_fee_cents = dto.overrideFeeCents;
      updates.gap_cents = dto.overrideFeeCents - scheduleFee;
      updates.subtotal_cents = dto.overrideFeeCents;
      const billingType = invoice['billing_type'] as string;
      const gstCents = billingType === 'private' ? Math.round(dto.overrideFeeCents * GST_RATE) : 0;
      updates.gst_cents = gstCents;
      updates.total_cents = dto.overrideFeeCents + gstCents;

      // Update line item too
      if (invoice.lineItems?.length) {
        const { db: dbConn } = await import('../../db/db');
        const firstItemId = invoice.lineItems[0]['id'] as string;
        await dbConn('invoice_line_items')
          .where({ invoice_id: invoiceId, id: firstItemId })
          .update({
            unit_price_cents: dto.overrideFeeCents,
            line_total_cents: dto.overrideFeeCents,
          });
      }
    }
  }

  await billingRepo.updateInvoice(clinicId, invoiceId, updates);

  const updated = await billingRepo.getInvoiceWithItems(clinicId, invoiceId);
  return mapInvoice(updated!);
}

/**
 * Mark an approved invoice as sent to the patient.
 * Also shares the invoice to the patient's Viva app.
 */
export async function markInvoiceSent(
  clinicId: string,
  invoiceId: string,
): Promise<void> {
  const invoice = await billingRepo.getInvoiceWithItems(clinicId, invoiceId);
  if (!invoice) return;

  await billingRepo.updateInvoice(clinicId, invoiceId, {
    status: 'sent',
    sent_at: new Date(),
    updated_at: new Date(),
  });

  try {
    const patientId = (invoice['patient_id'] as string | null) ?? null;
    const invoiceNumber = (invoice['invoice_number'] as string | null) ?? invoiceId;
    const totalCents = Number(invoice['total_cents'] ?? invoice['total_amount'] ?? 0);
    if (patientId) {
      await jobBus.enqueue('email', {
        type: 'billing_notice',
        clinicId,
        patientId,
        invoiceId,
        amountCents: Number.isFinite(totalCents) ? Math.max(0, Math.round(totalCents)) : undefined,
        currency: 'AUD',
        title: `Invoice issued — ${invoiceNumber}`,
        body: `Your invoice ${invoiceNumber} has been issued and is now available.`,
      });
    }
  } catch (err) {
    logger.warn({ err, clinicId, invoiceId }, 'Failed to enqueue invoice-issued email');
  }

  // Share invoice to patient's Viva app as a shared document
  const patientId = invoice['patient_id'] as string;
  if (patientId) {
    try {
      const { db: dbConn } = await import('../../db/db');
      const invoiceNum = invoice['invoice_number'] as string;
      const totalCents = Number(invoice['total_cents'] ?? invoice['total_amount'] ?? 0);
      const gapCents = Number(invoice['gap_cents'] ?? 0);

      await dbConn('patient_shared_documents').insert({
        clinic_id: clinicId,
        patient_id: patientId,
        title: `Invoice ${invoiceNum} — $${(totalCents / 100).toFixed(2)}${gapCents > 0 ? ` (Gap: $${(gapCents / 100).toFixed(2)})` : ''}`,
        doc_type: 'invoice',
        url: `billing/invoices/${invoiceId}`,
        file_path: null,
        shared_by: invoice['clinician_id'] ?? invoice['approved_by_staff_id'] ?? null,
      });

      logger.info({ clinicId, invoiceId, patientId }, 'Invoice shared to patient Viva app');
    } catch (err) {
      logger.warn({ err, clinicId, invoiceId }, 'Failed to share invoice to Viva — invoice still marked as sent');
    }
  }
}
