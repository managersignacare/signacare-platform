async function writeFileIfChanged(absPath: string, content: string): Promise<boolean> {
  await fs.mkdir(path.dirname(absPath), { recursive: true });
  const existing = await fs.readFile(absPath, 'utf8').catch(() => null);
  if (existing === content) return false;
  await fs.writeFile(absPath, content, 'utf8');
  return true;
}

async function run(mode: Mode): Promise<void> {
  const dirty: string[] = [];
  for (const target of TARGETS) {
    const absPath = path.join(ROOT_DIR, target.relPath);
    const generated = renderDartDesignTokens(target.app);
    if (mode === 'write') {
      const changed = await writeFileIfChanged(absPath, generated);
      if (changed) {
        console.log(`updated ${target.relPath}`);
      } else {
        console.log(`no change ${target.relPath}`);
      }
      continue;
    }

    const existing = await fs.readFile(absPath, 'utf8').catch(() => null);
    if (existing !== generated) dirty.push(target.relPath);
  }

  if (mode === 'check') {
    if (dirty.length === 0) {
      console.log('OK: Dart design tokens are in sync with web SSoT.');
      return;
    }
    console.error('FAIL: Dart design token files are out of sync with web SSoT.');
    for (const rel of dirty) console.error(` - ${rel}`);
    console.error('Run: npm run tokens:generate:dart');
    process.exitCode = 1;
  }
}

function parseMode(argv: string[]): Mode {
  return argv.includes('--check') ? 'check' : 'write';
}

const mode = parseMode(process.argv.slice(2));
run(mode).catch((err) => {
  console.error(err);
  process.exitCode = 1;
});

export { renderDartDesignTokens, parseRemToPx, asDartColor };
