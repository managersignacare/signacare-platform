import fs from 'node:fs/promises';
import path from 'node:path';

import { FONT_SIZES, SEVERITY_COLORS, THEME_PALETTES, TOUCH_TARGETS, type ThemeId } from '../../apps/web/src/shared/theme/palettes';

type Mode = 'write' | 'check';
type TargetApp = 'mobile' | 'patient';

const ROOT_DIR = path.resolve(__dirname, '../..');

const TARGETS: ReadonlyArray<{ app: TargetApp; relPath: string }> = [
  { app: 'mobile', relPath: 'apps/mobile/lib/core/generated/design_tokens.g.dart' },
  { app: 'patient', relPath: 'apps/patient-app/lib/core/generated/design_tokens.g.dart' },
];

function parseRemToPx(remValue: string): number {
  if (!remValue.endsWith('rem')) {
    throw new Error(`Expected rem value, got: ${remValue}`);
  }
  const rem = Number.parseFloat(remValue.replace('rem', '').trim());
  if (!Number.isFinite(rem)) {
    throw new Error(`Invalid rem value: ${remValue}`);
  }
  return rem * 16;
}

function formatNumber(value: number): string {
  if (Number.isInteger(value)) return `${value}`;
  return `${value.toFixed(3).replace(/0+$/, '').replace(/\.$/, '')}`;
}

function asDartColor(hex: string): string {
  const normalized = hex.trim().replace(/^#/, '').toUpperCase();
  if (!/^[0-9A-F]{6}$/.test(normalized)) {
    throw new Error(`Expected 6-digit hex color, got: ${hex}`);
  }
  return `Color(0xFF${normalized})`;
}

function renderPaletteEntries(): string {
  return (Object.entries(THEME_PALETTES) as Array<[ThemeId, (typeof THEME_PALETTES)[ThemeId]]>)
    .map(([themeId, palette]) => `    '${themeId}': SignacareThemePaletteToken(
      primary: ${asDartColor(palette.primary)},
      onPrimary: ${asDartColor(palette.onPrimary)},
      secondary: ${asDartColor(palette.secondary)},
      sidebar: ${asDartColor(palette.sidebar)},
      sidebarText: ${asDartColor(palette.sidebarText)},
      background: ${asDartColor(palette.background)},
      paper: ${asDartColor(palette.paper)},
      text: ${asDartColor(palette.text)},
      accent: ${asDartColor(palette.accent)},
    ),`)
    .join('\n');
}

